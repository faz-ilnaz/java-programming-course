package com.kpfu.itis;

public class Invite {

	private int id;
	private int type;
	
	private int id_vacancy;
	private int id_CV;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getId_vacancy() {
		return id_vacancy;
	}
	public void setId_vacancy(int id_vacancy) {
		this.id_vacancy = id_vacancy;
	}
	public int getId_CV() {
		return id_CV;
	}
	public void setId_CV(int id_CV) {
		this.id_CV = id_CV;
	}
}
